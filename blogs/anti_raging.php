                                    <li><b>Positive Vibes Only</b>: Choose fun that doesn’t harm or inconvenience
                                        others—your actions make a difference.</li>
                                    <li><b>Anti-Ragging Team</b>: We create a team which is consist of the students of
                                        every classes and some teachers are guide them to handle this ragging problem.
                                    </li>
                                </ul>

                                <h3>How we solve this?</h3>
                                <p>At our college, we believe in creating an environment where every student feels safe
                                    and respected. To uphold this standard, we’ve established a dedicated team focused
                                    on managing and preventing raging within our facilities. This team works
                                    hand-in-hand with our teachers, fostering open communication to swiftly address any
                                    issues that arise.</p>
                                <ol class="order-list">
                                    <li>We also understand the importance of providing students with a direct line of
